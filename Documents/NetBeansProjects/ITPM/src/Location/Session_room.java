    private void table_update(){
        
        try {
            
            int c;
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/itpm","root","");
                pst = con.prepareStatement("select * from manage_rooms");
                ResultSet rs = pst.executeQuery();
                
                ResultSetMetaData rsd = rs.getMetaData();
                c = rsd.getColumnCount();
                
                DefaultTableModel d= (DefaultTableModel)jTable2.getModel();
                d.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1; i<=c; i++){
                        v2.add(rs.getString("ID"));
                        v2.add(rs.getString("sessionID"));
                        v2.add(rs.getString("select_session"));
                        v2.add(rs.getString("selected_session"));
                        v2.add(rs.getString("select_room"));
                        
                    }
                    d.addRow(v2);
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Session_room.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Session_room.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
        
        
    }
    
    
    
    
