        String sdsessions = sdsession.getText();
        String room = sroom.getSelectedItem().toString();
        
        if(Sid.getText().isEmpty()){
           
           jLabel6.setText("**Plase Enter The SESSION ID..!!");
           return;           
        }
         
             
         
         if(sdsession.getText().isEmpty()){
           //txtBname.setEnabled(true);
           jLabel8.setText("**Plase Enter The SESSION..!!");
           return;
           }
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/itpm","root","");
            pst=con.prepareStatement("update manage_rooms set sessionID=?,select_session=?,selected_session=?,select_room=? where ID=?");
            
            pst.setString(1,sid);
            pst.setString(2,session);
            pst.setString(3,sdsessions);
            pst.setString(4,room);
            pst.setInt(5,id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Room UpDateded...!");
            table_update();

            Sid.setText("");
            ssession.setSelectedIndex(-1);
            sdsession.setText("");
            sroom.setSelectedIndex(-1);
            Sid.requestFocus();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Session_room.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Session_room.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
        
