    private void txtcapKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcapKeyPressed
        // TODO add your handling code here:
        
        char c = evt.getKeyChar();
        
                //Character.isLetter(c)
        if(Character.isLetter(c)){
           txtcap.setEnabled(false);
           txtcap.setText("");
           jLabel8.setText("Plase Enter The Capacity..!!");
           
        }else{
            txtcap.setEditable(true);
            jLabel8.setText("");
        }
        txtcap.setEnabled(true);
    }//GEN-LAST:event_txtcapKeyPressed

