    private void table_update(){
        
        try {
            
            int c;
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/itpm","root","");
                pst = con.prepareStatement("select * from session_room");
                ResultSet rs = pst.executeQuery();
                
                ResultSetMetaData rsd = rs.getMetaData();
                c = rsd.getColumnCount();
                
                DefaultTableModel d= (DefaultTableModel)jTable1.getModel();
                d.setRowCount(0);
                
                while(rs.next())
                {
                    Vector v2 = new Vector();
                    
                    for(int i=1; i<=c; i++){
                        v2.add(rs.getString("ID"));
                        v2.add(rs.getString("Building_Name"));
                        v2.add(rs.getString("Room_Name"));
                        v2.add(rs.getString("Room_Type"));
                        v2.add(rs.getString("Capacity"));
                    }
                    d.addRow(v2);
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Session_location.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Session_location.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
        
        
    }
    
    
    
    
